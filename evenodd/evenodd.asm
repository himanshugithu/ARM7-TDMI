	AREA RESET, CODE,READONLY
	
	MOV R0,0X00 ; EVEN
	MOV R1,0X00 ; ODD
	MOV R2,0X0A ; COUNTER
	LDR R3,=0X40000000
	
BACK	LDR R4,[R3],#4
		RORS R4,#1
		BCC EVEN
		ADD R1,R1,#1
		B JUMP
EVEN	ADD R0,R0,#1
JUMP	SUBS R2,R2,#1
		BNE BACK
		STR R0,[R3],#4
		;ADD R3,R3,#1
		STR R1,[R3]
HERE	B HERE